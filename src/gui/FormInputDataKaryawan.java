    private void bsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsaveActionPerformed
        // TODO add your handling code here:
        String sql = "insert into karyawan values(?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, tid.getText());
            stat.setString(2, tnama.getText());

            String jkel="";
            if(rb1.isSelected()) jkel="Laki-laki";
            else jkel="Perempuan";
            stat.setString(3,jkel);

            stat.setString(4, talamat.getText());
            stat.setString(5, tdivisi.getText());
            stat.setString(6, tjabatan.getText());
            stat.setString(7, tnotel.getText());
            

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan ");
            kosong();
            tid.requestFocus();
            datatable();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan "+e);
        }
    }//GEN-LAST:event_bsaveActionPerformed

    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "update karyawan set nama=?, jenis_kelamin=?, alamat=?, divisi=?, jabatan=? , no_telp=?  where id_karyawan=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, tnama.getText());

            String jkel="";

            if(rb1.isSelected())jkel="Laki-laki";
            else jkel="Perempuan";
            stat.setString(2, jkel);

            stat.setString(3, talamat.getText());
            stat.setString(4, tdivisi.getText());
            stat.setString(5, tjabatan.getText());
            stat.setString(6, tnotel.getText());
            
            stat.setString(7,tid.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Update");
            kosong();
            tid.requestFocus();
            datatable();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal di Ubah "+e);
        }
    }//GEN-LAST:event_beditActionPerformed

    private void bdelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdelActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Hapus","Konfirmasi DIalog",JOptionPane.YES_NO_CANCEL_OPTION);
        if(ok==0){
            String sql = "Delete from karyawan where id_karyawan = '"+tid.getText()+"'";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Di Hapus ");
                kosong();
                tid.requestFocus();
                datatable();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null,"Data Gagal Dihapus "+e);
            }
        }
    }//GEN-LAST:event_bdelActionPerformed

