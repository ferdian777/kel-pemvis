    protected void datatable(){
        Object[] Baris = {"ID Karyawan","Nama Karyawan","Jenis Kelamin","Alamat","Divisi","Jabatan","No.telp"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelpasien.setModel(tabmode);
        String sql = "select * from karyawan";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("id_karyawan");
                String b = hasil.getString("nama");
                String c = hasil.getString("jenis_kelamin");
                String d = hasil.getString("alamat");
                String e = hasil.getString("divisi");
                String f = hasil.getString("jabatan");
                String g = hasil.getString("no_telp");
                
                String[] data ={a,b,c,d,e,f,g};
                tabmode.addRow(data);
            }
        }catch(Exception e){
            
        }
    }
    
